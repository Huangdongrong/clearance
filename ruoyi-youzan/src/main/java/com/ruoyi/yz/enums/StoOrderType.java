package com.ruoyi.yz.enums;

/**
 *
 * @author wmao
 */
public enum StoOrderType {
    ORDINARY("0", "普通"), COD("1", "COD"), DAOFU("2", "到付"), INTERNATIONAL("3", "国际"), OTHER("99", "其它");
    private String key;
    private String value;
    StoOrderType(String key, String value){
        this.key = key;
        this.value = value;
    }

    /**
     * @return the key
     */
    public String getKey() {
        return key;
    }

    /**
     * @param key the key to set
     */
    public void setKey(String key) {
        this.key = key;
    }

    /**
     * @return the value
     */
    public String getValue() {
        return value;
    }

    /**
     * @param value the value to set
     */
    public void setValue(String value) {
        this.value = value;
    }
}
