package com.ruoyi.yz.enums;

/**
 *
 * @author wmao
 */
public enum IdentityType {
    ID("1"), OTHER("2");

    private String key;

    IdentityType(String key) {
        this.key = key;
    }

    /**
     * @return the key
     */
    public String getKey() {
        return key;
    }

    /**
     * @param key the key to set
     */
    public void setKey(String key) {
        this.key = key;
    }
}
